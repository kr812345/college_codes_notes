#include <stdio.h>
#include <stdlib.h>

// Macro definition
#define PI 3.14159

// Function define
float calculate_area();

int main()
{
    // function call
    calculate_area();
    return 0;
}

// Function Definintion
float calculate_area()
{

    float area, radius;
    area = PI * radius * radius;

    return area;
}
